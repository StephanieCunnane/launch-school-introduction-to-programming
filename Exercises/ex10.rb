# Examples of nested collections

# Arrays can be hash values
wardrobe = {
  shirts: ['red', 'blue', 'fancy', 'green'],
  pants: ['dress', 'work', 'jeans', 'shorts'],
  jackets: ['fall', 'sports', 'winter']
}

# Arrays can contain hashes
team_members = [{name: "Stephanie", position: "pitcher"}, {name: "Natasha", position: "first base"}]
